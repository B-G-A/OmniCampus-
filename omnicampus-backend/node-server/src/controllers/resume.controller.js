const fs = require('fs');
const { PDFParse } = require('pdf-parse');
const axios = require('axios');
const ResumeHistory = require('../models/ResumeHistory');
const { AppError } = require('../middleware/errorHandler');

/**
 * POST /api/student/resume/analyze
 * Upload a PDF resume, analyze it via Ollama, and save to ResumeHistory.
 */
const analyzeResume = async (req, res, next) => {
  try {
    if (!req.file) {
      throw new AppError('No resume file uploaded.', 400, 'NO_FILE');
    }

    if (req.file.mimetype !== 'application/pdf') {
      throw new AppError('Only PDF files are supported for resume analysis.', 400, 'INVALID_FILE_TYPE');
    }

    // 1. Extract text from PDF
    const dataBuffer = fs.readFileSync(req.file.path);
    const parser = new PDFParse({ data: dataBuffer });
    const data = await parser.getText();
    const resumeText = data.text;

    if (!resumeText || resumeText.trim().length === 0) {
      throw new AppError('Could not extract text from the PDF.', 400, 'EMPTY_PDF');
    }

    // 2. Query Ollama for detailed evaluation
    const prompt = `
You are an expert ATS (Applicant Tracking System) and senior technical recruiter.
Analyze the following resume text and provide a JSON response with EXACTLY these keys:
- "overallScore": integer 0-100 representing overall resume quality.
- "atsScore": integer 0-100 for ATS compatibility (keyword density, formatting).
- "technicalSkillsScore": integer 0-100 for technical skill coverage.
- "projectsScore": integer 0-100 for project descriptions and impact.
- "experienceScore": integer 0-100 for work experience quality and relevance.
- "formattingScore": integer 0-100 for structure, layout, readability.
- "grammarScore": integer 0-100 for grammar, spelling, and language quality.
- "strengths": array of 2 short strings highlighting resume strengths.
- "weaknesses": array of 2 short strings highlighting resume weaknesses or gaps.
- "suggestions": array of 2 actionable improvement recommendations.

Make sure your response is ONLY valid JSON, without any markdown formatting like \`\`\`json.

Resume Text:
${resumeText.substring(0, 2500)}
`;

    let aiResponse;
    try {
      const result = await axios.post('http://localhost:11434/api/generate', {
        model: 'tinyllama',
        prompt: prompt,
        stream: false,
        format: 'json'
      });
      let rawResponse = result.data.response.trim();
      
      // Extract JSON block if tinyllama wrapped it in markdown or text
      const jsonMatch = rawResponse.match(/\{[\s\S]*\}/);
      if (jsonMatch) {
        rawResponse = jsonMatch[0];
      }
      
      // Basic repair for truncated JSON generated by tinyllama
      if (!rawResponse.endsWith('}')) {
        const lastQuote = rawResponse.lastIndexOf('"');
        if (lastQuote !== -1) {
          rawResponse = rawResponse.substring(0, lastQuote + 1);
        }
        rawResponse += ']}'; // Rough attempt to close arrays and object
      }
      
      try {
        aiResponse = JSON.parse(rawResponse);
        console.log('AI RESPONSE (parsed):', aiResponse);
      } catch (parseErr) {
        // Advanced cleanup if standard parse fails: remove trailing commas, fix brackets
        const cleaned = rawResponse.replace(/,(\s*[\]}])/g, '$1') + ']}';
        try {
          aiResponse = JSON.parse(cleaned);
          console.log('AI RESPONSE (repaired):', aiResponse);
        } catch (e2) {
          throw new Error('Failed to parse AI JSON response: ' + parseErr.message);
        }
      }
    } catch (err) {
      console.error("Ollama query failed:", err.message);
      aiResponse = null;
    }

    // 3. Normalize AI response — TinyLlama returns unpredictable structures
    const toInt = (val) => {
      if (typeof val === 'number') return Math.min(100, Math.max(0, Math.round(val)));
      if (typeof val === 'string') { const n = parseInt(val, 10); return isNaN(n) ? 0 : Math.min(100, Math.max(0, n)); }
      return 0;
    };

    const toStringArray = (val) => {
      if (!val) return [];
      if (!Array.isArray(val)) return typeof val === 'string' ? [val] : [];
      return val.map(item => {
        if (typeof item === 'string') return item;
        if (typeof item === 'object' && item !== null) {
          // Flatten objects like {title: "...", description: "..."} into a string
          return item.title || item.description || item.name || JSON.stringify(item);
        }
        return String(item);
      }).filter(s => s && s.length > 0);
    };

    const normalized = aiResponse ? {
      overallScore: toInt(aiResponse.overallScore || aiResponse.overall_score || aiResponse.score),
      atsScore: toInt(aiResponse.atsScore || aiResponse.ats_score || aiResponse.atsCompatibility),
      technicalSkillsScore: toInt(aiResponse.technicalSkillsScore || aiResponse.technical_skills_score || aiResponse.technicalSkills),
      projectsScore: toInt(aiResponse.projectsScore || aiResponse.projects_score || aiResponse.projects),
      experienceScore: toInt(aiResponse.experienceScore || aiResponse.experience_score || aiResponse.experiencceScore || aiResponse.experience),
      formattingScore: toInt(aiResponse.formattingScore || aiResponse.formatting_score || aiResponse.formatting),
      grammarScore: toInt(aiResponse.grammarScore || aiResponse.grammar_score || aiResponse.grammar),
      strengths: toStringArray(aiResponse.strengths || aiResponse.stretches || aiResponse.positives),
      weaknesses: toStringArray(aiResponse.weaknesses || aiResponse.weakness || aiResponse.gaps),
      suggestions: toStringArray(aiResponse.suggestions || aiResponse.recommendations || aiResponse.improvements || aiResponse.succesfulImprovements),
    } : {
      overallScore: 65, atsScore: 60, technicalSkillsScore: 70, projectsScore: 55,
      experienceScore: 60, formattingScore: 75, grammarScore: 80,
      strengths: ["Resume text was extracted successfully.", "Some recognizable keywords found."],
      weaknesses: ["Full AI analysis unavailable — Ollama was offline."],
      suggestions: ["Retry when AI service is running for a complete analysis."]
    };

    console.log('NORMALIZED RESPONSE:', normalized);

    // 4. Save to ResumeHistory
    const historyEntry = await ResumeHistory.create({
      student: req.user.id,
      fileName: req.file.originalname || 'resume.pdf',
      ...normalized,
    });

    // 4. Cleanup temp file
    if (fs.existsSync(req.file.path)) {
      fs.unlinkSync(req.file.path);
    }

    res.json({
      success: true,
      data: {
        _id: historyEntry._id,
        overallScore: historyEntry.overallScore,
        atsScore: historyEntry.atsScore,
        technicalSkillsScore: historyEntry.technicalSkillsScore,
        projectsScore: historyEntry.projectsScore,
        experienceScore: historyEntry.experienceScore,
        formattingScore: historyEntry.formattingScore,
        grammarScore: historyEntry.grammarScore,
        strengths: historyEntry.strengths,
        weaknesses: historyEntry.weaknesses,
        suggestions: historyEntry.suggestions,
        // Keep backward compat with old frontend fields
        score: historyEntry.overallScore,
        atsMatch: historyEntry.atsScore >= 80 ? 'High' : historyEntry.atsScore >= 60 ? 'Medium' : 'Low',
        positives: historyEntry.strengths,
        gaps: historyEntry.weaknesses,
        keywordsMatched: [],
        recommendations: (historyEntry.suggestions || []).join(' '),
      }
    });
  } catch (error) {
    if (req.file && fs.existsSync(req.file.path)) {
      fs.unlinkSync(req.file.path);
    }
    next(error);
  }
};

/**
 * GET /api/student/resume/history
 * Fetch all past resume analyses for the logged-in student.
 */
const getResumeHistory = async (req, res, next) => {
  try {
    const history = await ResumeHistory.find({ student: req.user.id })
      .sort({ createdAt: -1 })
      .limit(50);

    res.json({ success: true, data: history });
  } catch (error) {
    next(error);
  }
};

module.exports = {
  analyzeResume,
  getResumeHistory,
};
